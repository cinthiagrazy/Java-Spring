package br.com.generation.variaveis;

public class Variavel03 {

	public static void main(String[] args) {
		
		int var1 = 10;
		double var2 = 10.0;
				
		System.out.println("Valor oiginal da primeira variavel: " + var1);
		System.out.println("Valor original da segunda variavel: " + var2);

		var1 = var1 / 4;
		var2 = var2 / 4;
		
		System.out.println("Valor da primeira variavel pos divisão: " + var1);
		System.out.println("Valor da segunda variavel pos divisão: " + var2);
		
	
	}

}
